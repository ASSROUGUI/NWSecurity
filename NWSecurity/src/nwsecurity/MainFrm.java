    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        FileDialog fd = new FileDialog(this, "Choose a file", FileDialog.LOAD);
       try
       {
            fd.setFile("*.*");
            fd.setVisible(true);
            String filename = fd.getFile();
            if (filename != null)
            {
                System.out.println(fd.getDirectory()+filename);
                byte[] data = Files.readAllBytes(Paths.get(fd.getDirectory()+filename));
                 jLabel2.setText(Base64.getEncoder().encodeToString(data));
                 keyGeneratedOrLoaded=true;
                 secretKey = new SecretKeySpec(data, 0, data.length, "AES");
            }
       }  catch (Exception ex) {
        System .out.println(ex.getMessage());
    } 
    }//GEN-LAST:event_jButton5ActionPerformed

