    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    try {
    
      
      if(!keyGeneratedOrLoaded)
        {
            JOptionPane.showMessageDialog(null,"You must generate or  load a key");
            return;
        }
      
        String msg=jTextArea2.getText();
        if(msg.length()==0){
            JOptionPane.showMessageDialog(null,"You must write a message to encrypt it");
          return;
        }
        Cipher cipher = Cipher.getInstance("AES");
        cipher.init(Cipher.ENCRYPT_MODE, secretKey);
        
        ciphertext = cipher.doFinal(msg.getBytes());
        String result=new String(ciphertext);
        jTextArea1.setText(result);
        
         try (FileOutputStream fos = new FileOutputStream("encrypted.data")) {
               
              fos.write(ciphertext);
 
            }
        
    }  catch (Exception ex) {
        System .out.println(ex.getMessage());
    } 
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        if(!keyGeneratedOrLoaded)
        {
            JOptionPane.showMessageDialog(null,"You must load a key");
            return;
        }
        if(ciphertext==null||ciphertext.length==0){
            JOptionPane.showMessageDialog(null,"You load encrypted file to decrypt it");
          return;
        }
        Cipher cipher = Cipher.getInstance("AES");
        cipher.init(Cipher.DECRYPT_MODE, secretKey);
        //String encrypted=jTextArea1.getText();
       
        byte[] decrypted=cipher.doFinal(ciphertext);
                
        String res=new String(decrypted);
        jTextArea3.setText(res);
    }  catch (Exception ex) {
        System .out.println(ex.getMessage());
    } 
    }//GEN-LAST:event_jButton3ActionPerformed
